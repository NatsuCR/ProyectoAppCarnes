    public void cargarTablasClientes() {
        // Definir modelo para la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel() {

            // Filas y columnas no sean editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        // Establecemos t√≠tulos
        String titulos[] = {"ID", "Cedula", "Nombre", "Apellidos", "Rol"};
        modeloTabla.setColumnIdentifiers(titulos);

        // Traer la lista de usuarios
        List<Usuario> listaUsuarios = control.traerUsuario();

        if (listaUsuarios != null) {
            for (Usuario usu : listaUsuarios) {
                // Filtrar solo los que tienen rol "Cliente"
                if (usu.getUnRol().getNombreRol().equalsIgnoreCase("Usuario")) {
                    Object[] objeto = {usu.getId(), usu.getCedula(), usu.getNombre(), usu.getApellidos(), usu.getUnRol().getNombreRol()};
                    modeloTabla.addRow(objeto);
                }
            }
        }

        // Mostrar en la tabla
        TablaUsuarios.setModel(modeloTabla);
    }

